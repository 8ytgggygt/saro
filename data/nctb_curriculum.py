# Helper function to get subjects for a class
def get_subjects_for_class(class_name):
    """Get available subjects for a given class"""
    return list(NCTB_CURRICULUM.get(class_name, {}).keys())

# Helper function to get chapters for a subject
def get_chapters_for_subject(class_name, subject_name):
    """Get chapters for a subject in a given class"""
    return NCTB_CURRICULUM.get(class_name, {}).get(subject_name, {}).get('chapters', [])

# Helper function to get all available classes
def get_available_classes():
    """Get list of all available classes"""
    return list(NCTB_CURRICULUM.keys())